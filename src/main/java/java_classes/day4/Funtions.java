package java_classes.day4;

public class Funtions {

    public static void main(String[] args) {
        LoopsFun();
    }
        public static void LoopsFun(){

                int i,j;
                for (i =1; i<=10; i++){
                    for (j = 1; j<=10; j++)
                        System.out.print((i * j) +"    ");
                    System.out.println();
                }

            }
        }



